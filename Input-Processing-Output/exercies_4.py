# 4. Distance Traveled
# Assuming there are no accidents or delays, the distance that a car travels down the inter- state can be calculated with the following   formula:
# Distance 5 Speed 3 Time
#A car is traveling at 70 miles per hour. Write a program that displays the following:
#• The distance the car will travel in 6 hours
#• The distance the car will travel in 10 hours
#• The distance the car will travel in 15 hours 


# Given speed in miles per hour
speed = 70

# Calculate the distance for different times
distance_6_hours = speed * 6
distance_10_hours = speed * 10
distance_15_hours = speed * 15

# Display the results
print(f"The distance the car will travel in 6 hours: {distance_6_hours} miles")
print(f"The distance the car will travel in 10 hours: {distance_10_hours} miles")
print(f"The distance the car will travel in 15 hours: {distance_15_hours} miles")
